package estructuras;

public class Ejemplo {
	private String nombre;
	private int pos;
	
	public Ejemplo(String nombre, int pos){
		this.nombre = nombre;
		this.pos = pos;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public int getPos() {
		return pos;
	}

	public void setPos(int pos) {
		this.pos = pos;
	}
}
